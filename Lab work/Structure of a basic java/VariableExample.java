public class VariableExample {
    public static void main(String[] args) {
        int number = 10;
        double decimal = 5.5;
        char letter = 'A';
        boolean isTrue = true;
        System.out.println("Integer: " + number);
        System.out.println("Double: " + decimal);
        System.out.println("Character: " + letter);
        System.out.println("Boolean: " + isTrue);
    }
}